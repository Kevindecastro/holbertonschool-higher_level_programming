def update_dictionary(a_dictionary, key, value):
    # Ajouter ou mettre à jour la paire clé-valeur dans le dictionnaire

    a_dictionary[key] = value  # Attribuer la valeur à la clé spécifiée (si
    # la clé existe, elle sera mise à jour)

    # Retourner le dictionnaire mis à jour
    return a_dictionary  # Retourne le dictionnaire après modification
